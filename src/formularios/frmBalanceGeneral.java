    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

                double ingreso,gasto,utilidad,retiros = 0,capitalInicial = 0,capitalActual;
                double activo,pasivo;
        
        try {
            activo=consulta.sumaCargoActivo()-consulta.sumaAbonoActivo();            
            pasivo=consulta.sumaAbonoPasivo()-consulta.sumaCargoPasivo();
            txtActivo.setText(""+activo);
            txtPasivos.setText(""+pasivo);
            capitalInicial=consulta.sumaAbonoCapital();              
            ingreso=consulta.sumaIngreso();
            gasto=consulta.sumaGasto();            
            utilidad=ingreso-gasto;                      
            retiros=consulta.sumaCargoCapital();       
            capitalActual=capitalInicial+utilidad-retiros;
            txtCapital.setText(""+capitalActual);
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(frmEstadoResultados.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

