    private void btnConsultarSaldoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarSaldoActionPerformed
       
        char c=Codigo.charAt(0);
        double cargo,abono,saldo = 0;
        try {
            cargo=consulta.findId_Cargo(Codigo);
            abono=consulta.findId_Abono(Codigo);
         
            switch(c){
                case 'A':
                   saldo=cargo-abono; 
                break;
                case 'P':
                    saldo=abono-cargo;
                break;
                case 'C':
                    saldo=abono-cargo;
                break;
                case 'I':
                    saldo=abono-cargo;
                break;
                case 'G':
                    saldo=cargo-abono;
                break;
                default:
                break;
            } 
            
            txtCargo.setText(""+saldo);
            txtAbono.setText(""+saldo);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Mayorizacion.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_btnConsultarSaldoActionPerformed

