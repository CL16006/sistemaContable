    private void btnCierreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCierreActionPerformed
           
            try {
                ResultSet rs1,rs2;
                String fechaAsiento=txtFecha.getText();
                double cargo=Double.parseDouble(txtCargo.getText());
                double abono=Double.parseDouble(txtAbono.getText());
                String cuentaCargo=cmbCargo.getSelectedItem().toString();
                String cuentaAbono=cmbAbono.getSelectedItem().toString();
                String concepto=txtConcepto.getText();
                rs1=consulta.findIdByNombreSuperior(cuentaCargo);
                rs2=consulta.findIdByNombreSuperior(cuentaAbono);
                String idcargo = null,idabono = null;
                if(rs1.next()&&rs2.next()){
                    if(Codigo.charAt(0)=='I'){
                        idcargo=rs1.getString(1);
                    idabono=rs2.getString(1);
                    }else{
                        idabono=rs1.getString(1);
                    idcargo=rs2.getString(1);
                    }
                  
                }
                consulta.insertIntoLibroDiario(fechaAsiento, idcargo, idabono, cargo, abono, concepto);
                limpiar();
                
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(frmLibroDiario.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(frmLibroDiario.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_btnCierreActionPerformed

